#!/usr/bin/env python3
"""
Create evaluation tasks for ICED BIRD datasets.

This script creates evaluation tasks in text-to-sql-prototype for each BIRD
database using the connection mapping generated by create_connections.py.

Usage:
    export JWT_TOKEN="your_token_here"
    python create_eval_tasks.py --api-key-id 1
    python create_eval_tasks.py --token "your_token" --api-key-id 1 --eval-mode "pass_at_k"
"""

import argparse
import json
import os
import sys
from pathlib import Path
from typing import Dict, List, Optional

try:
    import requests
except ImportError:
    print("Error: requests library is required. Install with: pip install requests")
    sys.exit(1)


def get_jwt_token(args_token: Optional[str]) -> str:
    """Get JWT token from args or environment variable.

    Args:
        args_token: Token from command line arguments.

    Returns:
        JWT token string.

    Raises:
        SystemExit: If token is not provided.
    """
    token = args_token or os.environ.get("JWT_TOKEN")

    if not token:
        print("Error: JWT token is required.", file=sys.stderr)
        print("Set JWT_TOKEN environment variable or use --token option.", file=sys.stderr)
        sys.exit(1)

    return token


def load_mapping(data_dir: Path) -> Dict[str, int]:
    """Load db_id to connection_id mapping from file.

    Args:
        data_dir: Path to data directory.

    Returns:
        Dictionary mapping db_id to connection_id.
    """
    mapping_path = data_dir / "db_id_mapping.json"

    if not mapping_path.exists():
        print(f"Error: Mapping file not found: {mapping_path}", file=sys.stderr)
        print("Run create_connections.py first.", file=sys.stderr)
        sys.exit(1)

    with open(mapping_path, "r", encoding="utf-8") as f:
        mapping = json.load(f)

    return mapping


def create_eval_task(
    db_id: str,
    connection_id: int,
    token: str,
    api_key_id: int,
    base_url: str,
    data_dir: Path,
    eval_mode: str = "greedy_search",
    temperature: float = 0.7,
    max_tokens: int = 2000
) -> Optional[int]:
    """Create an evaluation task via API.

    Args:
        db_id: Database identifier.
        connection_id: Connection ID.
        token: JWT token.
        api_key_id: API Key ID.
        base_url: API base URL.
        data_dir: Path to data directory.
        eval_mode: Evaluation mode.
        temperature: Sampling temperature.
        max_tokens: Maximum tokens.

    Returns:
        Task ID if successful, None otherwise.
    """
    dataset_path = (data_dir / "bird_dev.json").resolve()

    if not dataset_path.exists():
        print(f"  [ERROR] Dataset file not found: {dataset_path}")
        return None

    # Convert to forward slashes for consistency
    dataset_path_str = str(dataset_path).replace("\\", "/")

    # Build payload
    payload = {
        "name": f"BIRD Dev - {db_id}",
        "dataset_type": "bird",
        "dataset_path": dataset_path_str,
        "connection_id": connection_id,
        "api_key_id": api_key_id,
        "temperature": temperature,
        "max_tokens": max_tokens,
        "eval_mode": eval_mode
    }

    # Add mode-specific parameters
    if eval_mode == "pass_at_k":
        payload["sampling_count"] = 8
    elif eval_mode == "majority_vote":
        payload["vote_count"] = 5
    elif eval_mode == "check_correct":
        payload["max_iterations"] = 3

    headers = {
        "Authorization": f"Bearer {token}",
        "Content-Type": "application/json"
    }

    try:
        response = requests.post(
            f"{base_url}/api/v1/eval/tasks",
            headers=headers,
            json=payload,
            timeout=30
        )

        if response.status_code == 201:
            task_id = response.json().get("id")
            print(f"  [OK] Created task {task_id} for {db_id}")
            return task_id
        elif response.status_code == 401:
            print(f"  [ERROR] Authentication failed. Check your JWT token.")
            return None
        elif response.status_code == 400:
            error_detail = response.json().get("detail", "Unknown error")
            print(f"  [ERROR] Bad request: {error_detail}")
            return None
        else:
            print(f"  [ERROR] Failed to create task for {db_id}: {response.status_code}")
            print(f"         Response: {response.text}")
            return None

    except requests.exceptions.ConnectionError:
        print(f"  [ERROR] Cannot connect to {base_url}. Is the backend running?")
        return None
    except requests.exceptions.Timeout:
        print(f"  [ERROR] Request timeout.")
        return None
    except Exception as e:
        print(f"  [ERROR] Exception: {e}")
        return None


def create_eval_tasks(
    token: str,
    api_key_id: int,
    base_url: str,
    data_dir: Path,
    eval_mode: str = "greedy_search",
    temperature: float = 0.7,
    max_tokens: int = 2000,
    db_filter: Optional[List[str]] = None
) -> tuple:
    """Create evaluation tasks for all databases.

    Args:
        token: JWT token.
        api_key_id: API Key ID.
        base_url: API base URL.
        data_dir: Path to data directory.
        eval_mode: Evaluation mode.
        temperature: Sampling temperature.
        max_tokens: Maximum tokens.
        db_filter: Optional list of db_ids to filter.

    Returns:
        Tuple of (success_mapping, failed_list).
    """
    mapping = load_mapping(data_dir)

    # Filter if specified
    if db_filter:
        mapping = {k: v for k, v in mapping.items() if k in db_filter}
        print(f"[INFO] Filtered to {len(mapping)} databases")

    print(f"[INFO] Creating evaluation tasks for {len(mapping)} databases")
    print(f"[INFO] Mode: {eval_mode}, Temperature: {temperature}, Max Tokens: {max_tokens}")

    success = {}
    failed = []

    for i, (db_id, connection_id) in enumerate(mapping.items(), 1):
        print(f"\n[{i}/{len(mapping)}] Creating eval task for {db_id}...")

        task_id = create_eval_task(
            db_id=db_id,
            connection_id=connection_id,
            token=token,
            api_key_id=api_key_id,
            base_url=base_url,
            data_dir=data_dir,
            eval_mode=eval_mode,
            temperature=temperature,
            max_tokens=max_tokens
        )

        if task_id:
            success[db_id] = task_id
        else:
            failed.append(db_id)

    return success, failed


def main():
    parser = argparse.ArgumentParser(
        description="Create evaluation tasks for ICED BIRD datasets",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Environment Variables:
  JWT_TOKEN    JWT token for API authentication (alternative to --token)

Evaluation Modes:
  greedy_search   Single SQL generation (default)
  pass_at_k       K samples, pass if any correct
  majority_vote   K samples, select by majority voting
  check_correct   Generate-check-correct iterations

Examples:
  export JWT_TOKEN="eyJ0eXAiOiJKV1QiLCJhbGc..."
  python create_eval_tasks.py --api-key-id 1

  python create_eval_tasks.py --token "eyJ..." --api-key-id 1 --eval-mode "pass_at_k"

  # Only create tasks for specific databases
  python create_eval_tasks.py --api-key-id 1 --db-filter california_schools financial
        """
    )

    default_data_dir = "D:/Working/paper/project/text-to-sql-prototype/backend/data/bird"

    parser.add_argument(
        "--token",
        type=str,
        default=None,
        help="JWT token for API authentication (or set JWT_TOKEN env var)"
    )

    parser.add_argument(
        "--api-key-id",
        type=int,
        required=True,
        help="API Key ID (required, get from settings page)"
    )

    parser.add_argument(
        "--base-url",
        type=str,
        default="http://localhost:8000",
        help="API base URL (default: http://localhost:8000)"
    )

    parser.add_argument(
        "--data-dir",
        type=str,
        default=default_data_dir,
        help=f"Path to data directory (default: {default_data_dir})"
    )

    parser.add_argument(
        "--eval-mode",
        type=str,
        default="greedy_search",
        choices=["greedy_search", "pass_at_k", "majority_vote", "check_correct"],
        help="Evaluation mode (default: greedy_search)"
    )

    parser.add_argument(
        "--temperature",
        type=float,
        default=0.7,
        help="Sampling temperature (default: 0.7)"
    )

    parser.add_argument(
        "--max-tokens",
        type=int,
        default=2000,
        help="Maximum tokens (default: 2000)"
    )

    parser.add_argument(
        "--db-filter",
        nargs="+",
        default=None,
        help="Filter to specific database IDs (space-separated)"
    )

    args = parser.parse_args()

    # Get JWT token
    token = get_jwt_token(args.token)

    data_dir = Path(args.data_dir)

    print("="*50)
    print("ICED BIRD Evaluation Task Creation Tool")
    print("="*50)
    print(f"\nAPI URL: {args.base_url}")
    print(f"Data directory: {data_dir}")
    print(f"API Key ID: {args.api_key_id}")

    # Create tasks
    success, failed = create_eval_tasks(
        token=token,
        api_key_id=args.api_key_id,
        base_url=args.base_url,
        data_dir=data_dir,
        eval_mode=args.eval_mode,
        temperature=args.temperature,
        max_tokens=args.max_tokens,
        db_filter=args.db_filter
    )

    # Summary
    total = len(success) + len(failed)
    print(f"\n{'='*50}")
    print("Summary:")
    print(f"  Total databases: {total}")
    print(f"  Successfully created: {len(success)}")
    print(f"  Failed: {len(failed)}")

    if success:
        print(f"\n  Task mapping:")
        for db_id, task_id in success.items():
            print(f"    {db_id}: task {task_id}")

    if failed:
        print(f"\n  Failed databases: {failed}")

    print(f"{'='*50}")

    # Web UI link
    print(f"\nYou can view tasks at: {args.base_url.replace(':8000', ':5173')}/evaluation")

    if failed:
        print("\n[WARNING] Some tasks failed. Check errors above.")
        sys.exit(1)
    else:
        print("\n[Done] All evaluation tasks created successfully!")


if __name__ == "__main__":
    main()
